    <h1>Hitvallás</h1>

    <p>Számunkra a legfontosabb a megkérdőjelezhetetlen és folyamatos törekvés,hogy minőségi termékeket készíthessünk.
        Hitünk szerint a legfontosabb az ízletes, minőségi anyagokból elkészített termék.
        Ezért filozófiánk, hogy a mestereink tanításainak tisztelete és a hagyományok tovább őrzése miatt,a túrósbatyu
        ezután is túrót fog tartalmazni,mert gyermekeink és mi is úgy szeretjük !


    </p>

    <h1> Így készítjük</h1>

    <p>

        Az Bécsi Pékség legnépszerűbb terméke, - a házi kenyér - a régi idők receptje alapján, kovászolással készül
        pékségünkben.
        A kovász biztosította természetességet lehetőség szerint nem keverjük a modern világ adta adalékokkal,a házi
        kenyér esetében csak természetes alapanyagokat használunk ! A kézi formázással szeretettel óvjuk a tésztát, és a
        megfelelő idő elérkezésekor szakembereink bevetik a kemencébe. A több év ízeít a falaiban őrző kemence már
        jól tudja mi szolgál örömünkre, az aranysárga ropogós cipó.
        Pékségünkben kizárólag azokat a folyamatokat gépesítettük, amelyek - a pékség dolgózóinak fáradozását, valamint
        a megnövekedett igényeket egyaránt figyelembe véve - a házilagosságot semmiképp nem sodorták veszélybe. A mai
        napig sok folyamatot manufakturálisan,kézi erővel készítünk,ami nyílván lassubb és költségesebb,de az eredmény a
        hitünk szerint jobb, és finomabb termék. A kezdetekkor 4-5 féle kenyeret készítettünk,ma a változó vásárlási
        szokásokat követve azóta már 17 különböző félét, ezekhez 10 különböző tésztát készítünk. Leveles tésztából lesz
        kakaóscsiga,túrósbatyu, és sok más tradiciónális pékségi sütemény,ezek 100 g - os termékek,melyek mindegyikében
        40 g minőségi tölteléket teszünk. Ez persze nehezen kel versenyre a nagy multi cégek kisebb termékeivel,de
        nálunk más értékrend alapján készítjük a termékeket. A 2015. évben végzett géppark felújítás lehetőséget nyújt
        egy szélesebb vásárlói igényeket is kielégítő termékskála folyamatos elkészítésére, - és szándékosan nem a
        gyártás szót használjuk. A pékség folyamatosan éjjel - nappal termel,bizonyos árukat időszakosan, a kiemelten
        népszerű termékeket igény szerint,bármely időszakban. A termelés az élelmiszer hígéniai előirások,és a
        társadalom idevonatkozó etikai kódexének betartásával történik. Jó étvagyat kíván a termékeinkhez, az Bécsi Pékség csapata !

    </p>
